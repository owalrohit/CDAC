//Write a program to calculate the sum of the first 50 natural numbers.


class SumOfNaturalNumbers{
	public static void main(String args[]){
		int n = 50;
		int sum = 0;
		
		for(int i=0;i<n;i++){
			sum +=n;
		}
		System.out.println("Sum of first 50 natural numbers is "+sum);

		
	}
}